function mergeHeavyTheory() {
  if (window.MedData) {
    // 1. Merge into MedData.topics (for System Workspaces)
    if (MedData.topics && window.MedExpandedTheory) {
      MedData.topics.forEach(topic => {
        const expansion = window.MedExpandedTheory[topic.id];
        if (expansion) {
          topic.summary = expansion;
        }
      });
      console.log("MedStudy Hub: System theory expanded successfully!");
    }

    // 2. Merge into MedData.anatomy3d (for 3D Anatomy tabs)
    if (MedData.anatomy3d && window.MedExpandedAnatomyTheory) {
      Object.keys(window.MedExpandedAnatomyTheory).forEach(organId => {
        if (MedData.anatomy3d[organId]) {
          Object.keys(window.MedExpandedAnatomyTheory[organId]).forEach(tabName => {
            MedData.anatomy3d[organId][tabName] = window.MedExpandedAnatomyTheory[organId][tabName];
          });
        }
      });
      console.log("MedStudy Hub: 3D anatomy theory expanded successfully!");
    }
  }
}

// Execute merge on load
mergeHeavyTheory();
